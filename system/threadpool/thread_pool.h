#ifndef __DAXIA_SYSTEM_THREADPOOL_H
#define __DAXIA_SYSTEM_THREADPOOL_H
#include <functional>
#include <thread>
#include <vector>
#include <future>
#include <boost/asio.hpp>

namespace daxia
{
	namespace system
	{
		class ThreadPool
		{
		public:
			ThreadPool(size_t threadCount = 0 // 线程数量。如为0则为cpu核数*2
				);
			~ThreadPool();
		public:
			// 分发一个任务,该任务加入任务列表，线程池将自动寻找一个空闲的线程执行该任务
			template<typename T>
			std::future<T> Post(std::function<void()> task)
			{
				std::packaged_task<void()> pt(task);
				ios_.post(pt);
			}

			// 分发一个任务，如果调用此方法的线程为线程池中的线程则立即执行，否则同Post。
			template<typename T>
			std::future<T> Dispatch(std::function<void()> task)
			{
				std::packaged_task<void()> pt(task);
				ios_.dispatch(std::ref(pt));
			}
		public:
			// 获取CPU核心数量
			static size_t GetCpuCoreCount();
		private:
			std::vector<std::thread> threads_;
			boost::asio::io_service ios_;
		private:
			void start(size_t count);
			void stop();
		};

	}
}

#endif // !__DAXIA_SYSTEM_THREADPOOL_H